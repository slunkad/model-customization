// ─── Score helpers ────────────────────────────────────────────────────────────

function mockScore(metricId: string, sampleIdx: number): number {
  const seed = metricId.charCodeAt(0) + metricId.length + sampleIdx * 17;
  return parseFloat(((Math.sin(seed) * 0.5 + 0.5) * 0.8 + 0.1).toFixed(4));
}

function avgScore(metricId: string): number {
  const n = mockEvalResults.perSample.length;
  let sum = 0;
  for (let i = 0; i < n; i++) sum += mockScore(metricId, i);
  return sum / n;
}

function formatScore(metricId: string, score: number): string {
  if (['toxicity', 'bias-score', 'fairness-score', 'privacy-score', 'sensitive-data-leakage'].includes(metricId)) {
    return score.toFixed(4);
  }
  return `${Math.round(score * 100)}%`;
}

// ─── Problem generation ───────────────────────────────────────────────────────

interface Problem {
  severity: 'high' | 'medium' | 'low';
  description: string;
  tags: string[];
}

function getProblems(metricId: string): Problem[] {
  const metric = METRICS.find(m => m.id === metricId);
  if (!metric) return [];
  const avg = avgScore(metricId);
  const problems: Problem[] = [];
  if (avg < 0.45) {
    problems.push({
      severity: 'high',
      description: `Model failed to satisfy the threshold 0.75 for metric '${metric.name}', with average score ${avg.toFixed(2)}. This metric measures how the model performs on ${metric.name.toLowerCase()}. Scores below 0.45 indicate significant reliability concerns.`,
      tags: [`Type: ${(METRIC_CHART_CATEGORY[metricId] ?? 'general').toLowerCase()}`, `Evaluator: ${metric.name}`],
    });
  }
  if (avg < 0.6) {
    problems.push({
      severity: 'medium',
      description: `Model failed to satisfy the threshold 0.75 for metric '${metric.name}', with average score ${avg.toFixed(2)}. ${metric.name} metric: ${metric.description.split('.')[0]}.`,
      tags: [`Type: ${(METRIC_CHART_CATEGORY[metricId] ?? 'general').toLowerCase()}`, `Evaluator: ${metric.name}`],
    });
  }
  return problems;
}

function problemCounts(metricId: string): [number, number, number] {
  const ps = getProblems(metricId);
  return [
    ps.filter(p => p.severity === 'high').length,
    ps.filter(p => p.severity === 'medium').length,
    ps.filter(p => p.severity === 'low').length,
  ];
}

