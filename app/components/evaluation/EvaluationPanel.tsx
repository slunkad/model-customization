function CategoryGroup({
  group,
  selected,
  onToggle,
  onClearGroup,
}: {
  group: typeof CATEGORY_GROUPS[number];
  selected: Set<string>;
  onToggle: (item: string) => void;
  onClearGroup: (groupItems: string[]) => void;
}) {
  const [collapsed, setCollapsed] = useState(false);
  const hasSelection = group.items.some(i => selected.has(i));

  return (
    <div className="mb-4">
      <div className="flex items-center justify-between mb-2">
        <span className="text-xs font-bold text-slate-700 uppercase tracking-wide">{group.label}</span>
        {hasSelection && (
          <button
            onClick={() => onClearGroup(group.items)}
            className="text-[10px] text-blue-600 hover:text-blue-700 flex items-center gap-0.5"
          >
            <XCircle className="w-3 h-3" /> Clear
          </button>
        )}
      </div>

      {!collapsed && (
        <div className="space-y-1">
          {group.items.map(item => (
            <label key={item} className="flex items-center gap-2 py-1 cursor-pointer group">
              <input
                type="checkbox"
                checked={selected.has(item)}
                onChange={() => onToggle(item)}
                className="w-3.5 h-3.5 rounded border-slate-300 text-blue-600 cursor-pointer accent-blue-600"
              />
              <span className={cn(
                'text-xs flex-1 truncate',
                selected.has(item) ? 'text-slate-900 font-medium' : 'text-slate-600 group-hover:text-slate-800'
              )}>
                {item}
              </span>
              <Info className="w-3.5 h-3.5 text-slate-300 group-hover:text-slate-400 flex-shrink-0" />
            </label>
          ))}
        </div>
      )}

      <button
        onClick={() => setCollapsed(c => !c)}
        className="mt-2 flex items-center gap-1 text-[10px] text-slate-400 hover:text-slate-600"
      >
        {collapsed ? <ChevronDown className="w-3 h-3" /> : <ChevronUp className="w-3 h-3" />}
      </button>

      <div className="mt-3 border-b border-slate-100" />
    </div>
  );
}

